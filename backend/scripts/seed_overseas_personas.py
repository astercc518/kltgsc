def run():
    with Session(engine) as session:
        # 插入人设
        existing_names = {p.name for p in session.exec(select(AIPersona)).all()}
        new_personas = 0
        for data in PERSONAS:
            if data["name"] in existing_names:
                print(f"  跳过已存在: {data['name']}")
                continue
            persona = AIPersona(**data)
            session.add(persona)
            new_personas += 1
        session.commit()
        print(f"\n✅ 新增 {new_personas} 个 AI 人设")

        # 插入脚本
        existing_script_names = {s.name for s in session.exec(select(Script)).all()}
        new_scripts = 0
        for data in SCRIPTS:
            if data["name"] in existing_script_names:
                print(f"  跳过已存在脚本: {data['name']}")
                continue
            script = Script(**data)
            session.add(script)
            new_scripts += 1
        session.commit()
        print(f"✅ 新增 {new_scripts} 个炒群脚本")

        # 统计
        total_personas = len(session.exec(select(AIPersona)).all())
        total_scripts = len(session.exec(select(Script)).all())
        print(f"\n📊 当前总计: {total_personas} 个人设 | {total_scripts} 个脚本")


if __name__ == "__main__":
    run()
